namespace ILOG.J2CsMapping.Collections.Generics {

    /// <summary>
    /// .NET replacement for java ListIterator
    /// </summary>
    /// <typeparam name="T"></typeparam>
    public interface IListIterator<T> : IIterator<T> {
        /// <summary>
        /// 
        /// </summary>
        /// <returns></returns>
        bool HasPrevious();
        /// <summary>
        /// 
        /// </summary>
        /// <returns></returns>
        T Previous();
        /// <summary>
        /// 
        /// </summary>
        /// <returns></returns>
        int NextIndex();
        /// <summary>
        /// 
        /// </summary>
        /// <returns></returns>
        int PreviousIndex();
        /// <summary>
        /// 
        /// </summary>
        /// <param name="x"></param>
        void Set(T x);
        /// <summary>
        /// 
        /// </summary>
        /// <param name="x"></param>
        void Add(T x);
    }
}
