package com.ilog.translator.java2cs.configuration.options;

public class StringOptionBuilder implements Builder<OptionImpl<String>> {
	public void build(String value, OptionImpl<String> defaultValue) {
		defaultValue.setValue(value);
	}

	public String createStringValue(OptionImpl<String> option) {
		return option.getValue();
	}
}