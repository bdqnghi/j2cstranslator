package com.ilog.translator.java2cs.popup.actions;

public class MarkAsPartialAction extends AbstractMarkAsAction {

	/**
	 * Constructor for Action1.
	 */
	public MarkAsPartialAction() {
		super();
		applyOnType = true;
		applyOnMethod = false;
		applyOnField = false;
	}

	@Override
	public String getTagName() {
		return "partial";
	}

	@Override
	public String getTagValue() {
		return "true";
	}
}
